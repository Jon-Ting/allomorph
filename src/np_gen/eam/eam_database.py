import math
from dataclasses import dataclass


@dataclass
class AtType:
    name: str
    re: float
    fe: float
    rhoe: float
    rhos: float
    alpha: float
    beta: float
    A: float
    B: float
    cai: float
    ramda: float
    Fi0: float
    Fi1: float
    Fi2: float
    Fi3: float
    Fm0: float
    Fm1: float
    Fm2: float
    Fm3: float
    fnn: float
    Fn: float
    ielement: int
    amass: float
    Fm4: float
    beta1: float
    ramda1: float
    rhol: float
    rhoh: float

    def __post_init__(self):
        self.blat = math.sqrt(2.0) * self.re
        self.rhoin = self.rhol * self.rhoe
        self.rhoout = self.rhoh * self.rhoe


